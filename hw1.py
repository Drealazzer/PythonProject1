def узнатьоценку(балы):
    if 0 <= балы <= 49:
        return "незадовільно"
    elif 50 <= балы <= 69:
        return "задовільно"
    elif 70 <= балы <= 89:
        return "добре"
    elif 90 <= балы <= 100:
        return "відмінно"
    else:
        return "некоректне значення, введіть вашу бал (від 1 до 100) "

балы = int(input("Введіть кількість балів: "))
оценка = узнатьоценку(балы)
print("Ваша оцінка: " + оценка)
